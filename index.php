    <h2>Our Team:</h2>

    <p>Considering how well known the Burlington community is for the love of outdoor sports and open-minded
        culture, it is no surprise that the Sailing Team is one of the most popular and competitive club sports at
        UVM. We compete in the New England Intercollegiate Sailing Association (NEISA), which is undoubtedly the
        most competitive district in college sailing. The team attends an average of 4 to 6 regattas per weekend.
        While the competition for spots at top events each weekend is hot, the expansive schedule ensures that
        everyone who demonstrates commitment and hard work at practice, will have the opportunity to sail. Most
        regattas that we attend are either fleet or team racing in double-handed dinghies, but the team sails in a
        variety of sloop and single-handed regattas as well. The team is funded as a club sport through the Student
        Government Association at UVM, and fundraises actively to augment its budget each season. We were lucky
        enough to get a brand new fleet of 18 FJs in the spring of 2013 and will have a beautiful new sailing
        facility, shared with the Lake Champlain Community Sailing Center, starting in the fall of next year. </p>

    <h2>F.A.Q</h2>

    <h3>What does practice look like?</h3>

    <p>We practice four days each week. On Monday through Thursday we begin practice
        around 2:00 and work on drills through about 6:00 PM. We run drills that
        emphasize boat handling and boat speed daily and mix in practice races throughout the week.</p>

    <h3>How does traveling work?</h3>

    <p>On average, we travel to 4-6 regattas each weekend.
        Our coach makes the pairings and decides who will go to each
        event by the Tuesday before the event. On Friday, each group will
        drive to their respective venues after classes end. For the most part,
        the team competes at schools that are also in the
        NEISA (New EnglandIntercollegiate Sailing Association),
        which are usually a 3-5 hour drive away. On occasion we will drive to
        further venues for some intersectional events.</p>

    <h3>Is it hard to balance sailing with other commitments?</h3>

    <p>For some it can be an adjustment, and may seem over whelming at first.
        However, once things slow down after the first week or two,
        managing your time becomes rather easy. Many of our sailors are very active members
        (or even officers) of other clubs and organizations and have plenty of time for other activities,
        school work, and social life during the sailing season.</p>

    <h3>Can I come on a recruiting trip?</h3>

    <p>Absolutely! We encourage recruits to come during the fall of their senior year of high school.
        On this trip you would have the opportunity to stay with a Freshman or Sophomore in the dorms,
        sit in on classes that are part of your expected major, watch a practice, and meet with our coach.
        In addition to getting a feel for the team and UVM's academics, you would be able to enjoy all
        Burlington has to offer.</p>

    <h3>Should I bring sailing gear on this trip?</h3>

    <p>Yes, though you may not be able to sail, Lake Champlain can sometimes offer chilly
    conditions and you want to be comfortable on the coach boat during practice.
    (We recommend you bring spray gear, a life jacket, proper shoes and appropriate layers.)</p>

    <h3>Do we have any team workouts?</h3>

    <p>We have workouts twice each week. On Tuesdays and Thursdays we meet in the gym.
        We do a variety of exercises that place emphasis on improving agility, strength,
        and endurance. We meet at 7:00AM so classes usually aren't a conflict, however
        if you are one of the poor souls who has a class that begins at 8:00 then you can
        leave a few minutes early to make it on time.</p>


    <h3>How long is your season?</h3>

    <p>We sail from September until November, and then begin again as soon as the ice thaws, usually in late March.
        As a substitute for lost time in March, we have a spring training trip during Spring Break, and often train
        with Boston teams on Fridays.</p>

    <h3>Does UVM offer scholarships for sailing?</h3>

    <p>The University of Vermont does not offer scholarships for sailing; in fact, under ICSA rules no sailor may
        receive financial support based on sailing ability.</p>

    <h3>What is Burlington like?</h3>

    <p>Burlington is a great town - it is in the truest essence a college town, complete with four local colleges.
        The surrounding scenery is absolutely breathtaking, and one can pursue a plethora of outdoor sports. As soon
        as the season ends, our sailors often head to the mountains to enjoy the winter season. With such great
        surroundings and such fun people, the city of Burlington is really a great place to live.</p>
